//Сложение
function addition (a, b) {
    let c = a + b;
    return c;
};

//Вычитание
function subtract (a, b) {
    let c = a - b;
    return c;
};

//Умножение
function multiplier (a, b) {
    let c = a * b;
    return c;
};

//Деление
function division (a ,b) {
    let c = a / b;
    return c;
};

console.log(addition(2,2)); // 4
console.log(subtract(2,2)); // 0
console.log(multiplier(2,2)); // 4
